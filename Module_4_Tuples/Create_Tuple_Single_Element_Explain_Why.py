a = (1,)
print(a)
# Explanation:
# In Python, to create a tuple with a single element, you must include a trailing comma after the element.
# Without the comma, Python interprets the parentheses as just grouping parentheses and not as a tuple.
# For example, (1) is just the integer 1 enclosed in parentheses, whereas (1,) is a tuple containing the single element 1.